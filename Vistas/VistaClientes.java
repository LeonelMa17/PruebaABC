    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
         if(txtid.getText().length() < 1) {
           JOptionPane.showMessageDialog(null,"Seleccione un registro de la tabla");  
       } else {
             cliente c = new cliente();
             c.setId_cliente(Integer.parseInt(txtid.getText()));
             c.setNombre(txtnombre.getText());
             c.setApellidos(txtapellidos.getText());
             c.setDireccion(txtdireccion.getText());
             c.setTelefono(txttelefono.getText());

             if(negocio.ActualizarCliente(c)) {
                JOptionPane.showMessageDialog(null, "Se actualizó correctamente");
                Consultar();
                Limpiar();
             }
         }
    }//GEN-LAST:event_btnactualizarActionPerformed

    public boolean Verificar(){
        if(txtnombre.getText().length() < 1 || txtapellidos.getText().length() < 1 ||
           txtdireccion.getText().length() < 1 || txttelefono.getText().length() < 1) {
            return true;
        } else {
            return false;
        }
    }
    
    
