    void Consultar(){
        Cargar();
        clientes = new ArrayList<cliente>();
        clientes = negocio.ConsultarClientes();
        String[] registros = new String[5];
        for(int i = 0; i < clientes.size(); i++){
            registros[0] = String.valueOf(clientes.get(i).getId_cliente());
            registros[1] = clientes.get(i).getNombre();
            registros[2] = clientes.get(i).getApellidos();
            registros[3] = clientes.get(i).getDireccion();
            registros[4] = clientes.get(i).getTelefono();
            model.addRow(registros);
        }
    }
    
    void Buscar(String valor) {
        Cargar();
        clientes = new ArrayList<cliente>();
        clientes = negocio.Buscar(valor);
        String[] registros = new String[5];
        for(int i = 0; i < clientes.size(); i++){
            registros[0] = String.valueOf(clientes.get(i).getId_cliente());
            registros[1] = clientes.get(i).getNombre();
            registros[2] = clientes.get(i).getApellidos();
            registros[3] = clientes.get(i).getDireccion();
            registros[4] = clientes.get(i).getTelefono();
            model.addRow(registros);
        }
    }
    
    void Cargar() {
    String [] titulos={"Id Cliente","Nombre","Apellidos","Direccion","Telefono"};
    model = new DefaultTableModel(null,titulos);
    tabla.setModel(model);
    }
    
